/* เขียน SQL statement เเสดงรหัสอสังหาริมทรัพย์ เเละความคิดเห็น
   ของลูกค้าที่มีต่ออสังหาริมทรัพย์ จากข้อมูลเกี่ยวกับอสังหาริมทรัพย์ที่
   ลูกค้าสนใจ โดยแสดงเฉพาะความเห็นของลูกค้าที่มีคำว่า '2%' อยู่ใน
   ตำแหน่งใดก็ได้ของความคิดเห็น */

