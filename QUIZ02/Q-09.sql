/* จงเขียน SQL Statement เพื่อแสดงข้อมูล ชื่อสินค้า, น้ำหนักสินค้า และ
   รหัสของเรือจากตารางข้อมูลของสินค้า โดยให้แสดงเฉพาะสินค้าที่มีลักษณะดังนี้
   - มีน้ำหนักระหว่าง 10 และ 2500 กก.
   - อยู่บนเรือที่มีรหัสของเรือเป็น 10, 42989, 63762, 6261 หรือ 166841
   หมายเหตุ: ต้องใช้ BETWEEN และห้ามใช้ OR */

SELECT item_name, weight, ship_id
FROM cargo
WHERE (weight BETWEEN 10 AND 2500) AND (ship_id IN (10, 42989, 63762, 6261, 166841));
